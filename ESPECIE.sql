CREATE TRIGGER BEFORE_UPDATE_ESPECIE
  BEFORE UPDATE ON PROGRA_2.ESPECIE FOR EACH ROW
BEGIN
	SET NEW.USUARIO_ULTIMA_MODIFICACION:= current_user();
	SET NEW.FEC_ULTIMA_MODIFICACION:= curdate();
END$$